package Gun07;

public class _04_StringLastIndexOf {
    public static void main(String[] args) {
        //indexof gibi çalışır bu aranaya baştan başlar
        //lastindexof ise sondan aramaya başlar

        //         0123456
        String s1="Merhaba";
        System.out.println("s1.indexOf(a) = " + s1.indexOf("a"));             //ilk a nın indexini bulur
        System.out.println("s1.lastIndexOf(a) = " + s1.lastIndexOf("a"));     //en sondaki a harfinin indexini bulur.





    }
}
